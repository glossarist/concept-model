#!/usr/bin/env node
// Codegen: emit predicate constants for TypeScript and Ruby from the
// canonical JSON-LD context + OWL ontology.
//
// Inputs:
//   ontologies/glossarist.context.jsonld   (term → CURIE map)
//   ontologies/glossarist.ttl               (class/property declarations)
//
// Outputs (both committed, not gitignored, so consumers don't need a build):
//   dist/js/predicates.js                   (ESM)
//   dist/js/predicates.d.ts                 (typed)
//   dist/ruby/lib/glossarist/concept_model/predicates.rb
//   dist/js/prefixes.js                     (prefix map for serializers)
//   dist/ruby/lib/glossarist/concept_model/prefixes.rb
//
// Run: `node scripts/gen-predicates.mjs` (also wired into `npm run build`).

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const CTX_PATH = resolve(ROOT, 'ontologies/glossarist.context.jsonld');
const TTL_PATH = resolve(ROOT, 'ontologies/glossarist.ttl');
const OUT_JS = resolve(ROOT, 'dist/js');
const OUT_RUBY = resolve(ROOT, 'dist/ruby/lib/glossarist/concept_model');

mkdirSync(OUT_JS, { recursive: true });
mkdirSync(OUT_RUBY, { recursive: true });

// ── 1. Parse JSON-LD context ────────────────────────────────────────────

const ctx = JSON.parse(readFileSync(CTX_PATH, 'utf8'))['@context'];

if (!ctx || typeof ctx !== 'object') {
  console.error('FATAL: @context missing or not an object');
  process.exit(1);
}

// Partition: namespace bindings (string vals ending in `/` or `#`) vs
// term definitions (object vals, or string vals that look like CURIEs).
function isNamespaceUri(s) {
  return typeof s === 'string' && (s.endsWith('/') || s.endsWith('#'));
}

const namespaces = {};     // prefix → URI (e.g. { skos: '...skos/core#' })
const terms = {};          // termName → { curie, type, container, language }

for (const [key, val] of Object.entries(ctx)) {
  if (key.startsWith('@')) continue;        // @version, @vocab etc.
  if (typeof val === 'string') {
    if (isNamespaceUri(val)) {
      namespaces[key] = val;
    } else {
      // Bare CURIE term: { "Concept": "gloss:Concept" }
      terms[key] = { '@id': val };
    }
  } else if (val && typeof val === 'object') {
    terms[key] = val;
  }
}

// ── 2. Resolve CURIEs to full URIs ──────────────────────────────────────

function resolveCurie(curie) {
  if (typeof curie !== 'string') return null;
  if (curie.startsWith('http://') || curie.startsWith('https://')) return curie;
  const idx = curie.indexOf(':');
  if (idx === -1) return null;
  const prefix = curie.slice(0, idx);
  const local = curie.slice(idx + 1);
  const base = namespaces[prefix];
  if (!base) return null;
  return base + local;
}

// Group terms by their source namespace for cleaner output
const grouped = {};  // prefix → { termName → fullUri }
for (const [term, def] of Object.entries(terms)) {
  const curie = typeof def === 'string' ? def : def['@id'];
  if (!curie) continue;
  if (curie.startsWith('@')) continue;        // JSON-LD keywords (@type, @id, …)
  const idx = curie.indexOf(':');
  if (idx === -1) continue;
  const prefix = curie.slice(0, idx);
  const uri = resolveCurie(curie);
  if (!uri) {
    console.warn(`WARN: cannot resolve ${term} → ${curie}`);
    continue;
  }
  if (!grouped[prefix]) grouped[prefix] = {};
  grouped[prefix][term] = uri;
}

// Also fold in the bare namespace URIs as `namespace` constants
// (e.g., PRED.gloss.$ns → 'https://www.glossarist.org/ontologies/')

// ── 3. Emit TypeScript ──────────────────────────────────────────────────

function tsConst(name, uri) {
  return `    ${name}: ${JSON.stringify(uri)} as const,`;
}

let ts = '';
ts += '// AUTO-GENERATED by scripts/gen-predicates.mjs. Do not edit.\n';
ts += '// Source: ontologies/glossarist.context.jsonld\n';
ts += '// Regenerate via `npm run build`.\n\n';
ts += '// eslint-disable-next-line @typescript-eslint/no-redeclare\n';
ts += 'export const PRED = {\n';

for (const [prefix, terms] of Object.entries(grouped)) {
  ts += `  ${prefix}: {\n`;
  for (const [term, uri] of Object.entries(terms)) {
    ts += tsConst(term, uri) + '\n';
  }
  if (namespaces[prefix]) {
    ts += tsConst('$ns', namespaces[prefix]) + '\n';
  }
  ts += `  } as const,\n\n`;
}
ts += '} as const;\n\n';

// Predicate type — union of all URIs for compile-time validation
ts += 'export type Predicate =\n';
const allUris = [];
for (const terms of Object.values(grouped)) {
  for (const uri of Object.values(terms)) allUris.push(uri);
}
for (const uri of allUris) {
  ts += `  | ${JSON.stringify(uri)}\n`;
}
ts += ';\n\n';

// Prefix map (for RDF serializers)
ts += 'export const PREFIXES: Record<string, string> = {\n';
for (const [prefix, uri] of Object.entries(namespaces)) {
  ts += `  ${JSON.stringify(prefix)}: ${JSON.stringify(uri)},\n`;
}
ts += '};\n';

writeFileSync(resolve(OUT_JS, 'predicates.js'), ts.replace(/as const/g, '') + '\nexport { PRED, PREFIXES };\n');
writeFileSync(resolve(OUT_JS, 'predicates.d.ts'), ts);

// ── 4. Emit Ruby ────────────────────────────────────────────────────────

function snakeCase(name) {
  return name.replace(/([A-Z])/g, '_$1').toLowerCase().replace(/^_/, '');
}

function isClassName(name) {
  // JSON-LD context convention: class declarations use PascalCase,
  // predicates use camelCase or lowercase. First-letter case distinguishes.
  return /^[A-Z]/.test(name);
}

function rubyClassConst(name, uri) {
  return `        ${name}: ${JSON.stringify(uri)},`;
}

function rubyPredConst(name, uri) {
  return `        ${snakeCase(name)}: ${JSON.stringify(uri)},`;
}

let rb = '';
rb += '# AUTO-GENERATED by scripts/gen-predicates.mjs. Do not edit.\n';
rb += '# Source: ontologies/glossarist.context.jsonld\n';
rb += '# Regenerate via `bundle exec rake build`.\n\n';
rb += 'module Glossarist\n';
rb += '  module ConceptModel\n';
rb += '    module PRED\n';

for (const [prefix, termsByOrigName] of Object.entries(grouped)) {
  const mod = prefix.toUpperCase().replace(/-/g, '_');
  rb += `      ${mod} = {\n`;
  for (const [term, uri] of Object.entries(termsByOrigName)) {
    if (isClassName(term)) {
      rb += rubyClassConst(term, uri) + '\n';
    } else {
      rb += rubyPredConst(term, uri) + '\n';
    }
  }
  if (namespaces[prefix]) {
    rb += rubyPredConst('ns', namespaces[prefix]) + '\n';
  }
  rb += '      }.freeze\n\n';
}

// Prefix map — string keys, since RDF serializers look up by string prefix.
rb += '      PREFIXES = {\n';
for (const [prefix, uri] of Object.entries(namespaces)) {
  rb += `        ${JSON.stringify(prefix)} => ${JSON.stringify(uri)},\n`;
}
rb += '      }.freeze\n';
rb += '    end\n';
rb += '  end\n';
rb += 'end\n';

writeFileSync(resolve(OUT_RUBY, 'predicates.rb'), rb);

// ── 5. Report ───────────────────────────────────────────────────────────

const termCount = Object.values(grouped).reduce((n, t) => n + Object.keys(t).length, 0);
console.log(`Generated predicate constants:`);
console.log(`  ${termCount} terms across ${Object.keys(grouped).length} namespaces`);
console.log(`  → ${resolve(OUT_JS, 'predicates.js')}`);
console.log(`  → ${resolve(OUT_JS, 'predicates.d.ts')}`);
console.log(`  → ${resolve(OUT_RUBY, 'predicates.rb')}`);
console.log(`  ${Object.keys(namespaces).length} prefixes`);
console.log(`  → dist/js/prefixes.js (embedded in predicates.js)`);
